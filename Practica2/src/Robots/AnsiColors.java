package Robots;

/**
 * Clase que contiene constantes para los códigos de colores ANSI.
 * Estos códigos se utilizan para cambiar el color del texto en la terminal.
 */
public class AnsiColors {
    public static final String RESET = "\u001B[0m";
    public static final String RED = "\u001B[31m";
    public static final String ORANGE = "\u001B[38;2;255;165;0m"; 
    public static final String BLUE = "\u001B[34m";
    public static final String PURPLE = "\u001B[35m";
    public static final String GREEN = "\u001B[32m";

    public static final String YELLOW = "\u001B[33m";
    public static final String CORAL = "\u001B[38;2;165;42;42m"; 
    public static final String BROWN = "\u001B[38;2;139;69;19m";
       
}
